function getRandomImage(imageArray) {
    const randomIndex = Math.floor(Math.random() * imageArray.length);
    return imageArray[randomIndex];
}

function getRandomTranslatedText(array) {
    const lang = localStorage.getItem("lang") || "fr";
    const key = array[Math.floor(Math.random() * array.length)];

    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }

    return key;
}

function getRandomFeatures() {
    const keys = Object.keys(rSpecies);

    const shuffled = keys.sort(() => 0.5 - Math.random());

    return shuffled.slice(0, 2);
}

function spinCharacter() {
    let speed = 50;
    let spins = 20;

    const stack = document.getElementById("imageStack");
    const defaultLayer = document.getElementById("defaultLayer");

    function spin() {
        const selected = getRandomFeatures();

        const layers = [
            document.getElementById("layer1"),
            document.getElementById("layer2"),
            document.getElementById("layer3"),
            document.getElementById("layer4"),
        ];
        defaultLayer.style.display = "none";

        layers.forEach(layer => {
            layer.style.display = "none";
            layer.src = "";
        });

        selected.forEach((featureKey, index) => {
            const images = rSpecies[featureKey];
            const randomImg = getRandomImage(images);

            layers[index].src = randomImg;
            layers[index].style.display = "block";
        });

        spins--;

        if (spins > 0) {
            speed += 20;
            setTimeout(spin, speed);
        } else {
            stack.classList.remove("bounce");
            void stack.offsetWidth;
            stack.classList.add("bounce");
        }
    }

    spin();
}

function spinImage(imgElement, imageArray) {
    let speed = 50;
    let spins = 20;

    function spin() {
        imgElement.src = getRandomImage(imageArray);
        spins--;

        if (spins > 0) {
            speed += 20;
            setTimeout(spin, speed);
        } else {
            imgElement.classList.remove("bounce"); 
            void imgElement.offsetWidth;
            imgElement.classList.add("bounce");
        }
    }

    spin();
}

function spinText(textElement, textArray) {
    let speed = 50;
    let spins = 20;

    function spin() {
        textElement.textContent = getRandomTranslatedText(textArray);
        spins--;

        if (spins > 0) {
            speed += 20;
            setTimeout(spin, speed);
        } else {
            textElement.classList.remove("bounce");
            void textElement.offsetWidth;
            textElement.classList.add("bounce");
        }
    }

    spin();
}


